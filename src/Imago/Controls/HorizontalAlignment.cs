namespace Imago.Controls;

/// <summary>
/// Specifies the horizontal alignment of a control within its parent container.
/// </summary>
public enum HorizontalAlignment
{
    /// <summary>
    /// The control is aligned to the left side of its parent container.
    /// </summary>
    Left,

    /// <summary>
    /// The control is centered horizontally within its parent container.
    /// </summary>
    Center,

    /// <summary>
    /// The control is aligned to the right side of its parent container.
    /// </summary>
    Right,

    /// <summary>
    /// The control stretches to fill the full width of its parent container.
    /// </summary>
    Stretch,
}
