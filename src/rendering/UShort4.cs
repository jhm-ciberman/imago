namespace LifeSim.Rendering
{
    public struct UShort4
    {
        public ushort X;
        public ushort Y;
        public ushort Z;
        public ushort W;

        public UShort4(ushort x, ushort y, ushort z, ushort w)
        {
            this.X = x;
            this.Y = y;
            this.Z = z;
            this.W = w;
        }
    }
}