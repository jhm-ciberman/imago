namespace LifeSim.Imago.Controls;

/// <summary>
/// Specifies the dock position of a control within its parent container.
/// </summary>
public enum Dock
{
    /// <summary>
    /// The control is docked to the left edge of its parent container.
    /// </summary>
    Left,

    /// <summary>
    /// The control is docked to the top edge of its parent container.
    /// </summary>
    Top,

    /// <summary>
    /// The control is docked to the right edge of its parent container.
    /// </summary>
    Right,

    /// <summary>
    /// The control is docked to the bottom edge of its parent container.
    /// </summary>
    Bottom,
}
