namespace LifeSim.Imago.Controls;

/// <summary>
/// Represents the orientation of a control layout.
/// </summary>
public enum Orientation
{
    /// <summary>
    /// Horizontal orientation, where elements are arranged left to right.
    /// </summary>
    Horizontal,

    /// <summary>
    /// Vertical orientation, where elements are arranged top to bottom.
    /// </summary>
    Vertical
}
