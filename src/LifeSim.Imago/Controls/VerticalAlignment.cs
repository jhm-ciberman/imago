namespace LifeSim.Imago.Controls;

/// <summary>
/// Specifies the vertical alignment of a control within its parent container.
/// </summary>
public enum VerticalAlignment
{
    /// <summary>
    /// The control is aligned to the top of its parent container.
    /// </summary>
    Top,

    /// <summary>
    /// The control is centered vertically within its parent container.
    /// </summary>
    Center,

    /// <summary>
    /// The control is aligned to the bottom of its parent container.
    /// </summary>
    Bottom,

    /// <summary>
    /// The control stretches to fill the full height of its parent container.
    /// </summary>
    Stretch,
}
